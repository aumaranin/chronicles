package bmstu.chronicles.models;

public class MountAsc
{
    private String mountain_name;
    private String ascension_name;
    private String ascension_date;

    public String getMountain_name()
    {
        return mountain_name;
    }

    public void setMountain_name(String mountain_name)
    {
        this.mountain_name = mountain_name;
    }

    public String getAscension_name()
    {
        return ascension_name;
    }

    public void setAscension_name(String ascension_name)
    {
        this.ascension_name = ascension_name;
    }

    public String getAscension_date()
    {
        return ascension_date;
    }

    public void setAscension_date(String ascension_date)
    {
        this.ascension_date = ascension_date;
    }
}
